package com.lishu.bike.model;


public class AppInfoDetailModel extends BaseModel {
    private String infoTitle;//标题,
    private String publishTime;//发布时间，yyyyMMddHHmmss,
    private String infoContent;//内容,
    private String publishUserName;//发布人

    public String getInfoTitle() {
        return infoTitle;
    }

    public void setInfoTitle(String infoTitle) {
        this.infoTitle = infoTitle;
    }

    public String getPublishTime() {
        return publishTime;
    }

    public void setPublishTime(String publishTime) {
        this.publishTime = publishTime;
    }

    public String getInfoContent() {
        return infoContent;
    }

    public void setInfoContent(String infoContent) {
        this.infoContent = infoContent;
    }

    public String getPublishUserName() {
        return publishUserName;
    }

    public void setPublishUserName(String publishUserName) {
        this.publishUserName = publishUserName;
    }
}
